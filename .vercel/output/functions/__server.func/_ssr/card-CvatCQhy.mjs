function getUser() {
  if (typeof window === "undefined") return null;
  try {
    const raw = localStorage.getItem(AUTH_KEY);
    return raw ? JSON.parse(raw) : null;
  } catch {
    return null;
  }
}
function setUser(u) {
  if (typeof window === "undefined") return;
  if (u) localStorage.setItem(AUTH_KEY, JSON.stringify(u));
  else localStorage.removeItem(AUTH_KEY);
  window.dispatchEvent(new Event("npms-auth"));
}
async function loginUser(email, password) {
  try {
    const res = await authenticateUser({ data: { email, password } });
    if (res.success && res.user) {
      setUser(res.user);
      return res.user;
    }
  } catch (e) {
    console.error("Auth server function error:", e);
  }
  return null;
}
function useUser() {
  const [user, set] = reactExports.useState(null);
  reactExports.useEffect(() => {
    set(getUser());
    const h = () => set(getUser());
    window.addEventListener("npms-auth", h);
    window.addEventListener("storage", h);
    return () => {
      window.removeEventListener("npms-auth", h);
      window.removeEventListener("storage", h);
    };
  }, []);
  return user;
}
